    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int selectedRow = tableShowtime.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this,"Vui lòng chọn suất chiếu cần xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this,"Bạn có chắc muốn xóa suất chiếu này");
        if(confirm == JOptionPane.YES_OPTION){
            String id = (String) model.getValueAt(selectedRow,0);
            try {
                showtimeDAO.deleteShowtime(id);
                JOptionPane.showMessageDialog(this,"Xóa thành công");
                getTable();
                ClearForm();
            } catch (HeadlessException | ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(this,"Lỗi xóa suất chiếu");
            }
        }        
    }//GEN-LAST:event_deleteBtnActionPerformed

