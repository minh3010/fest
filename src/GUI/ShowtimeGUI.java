    private void loadMovie(){
        try {
            MovieComboBox.removeAllItems();
            List<Movie> movies = movieDAO.findAll();           
            for (Movie movie : movies) {
               if(movie.isShowing()==true){ 
                 MovieComboBox.addItem(movie.getId() + " - " + movie.getTitle());
               } 
            }
        } catch(SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(this,"Lỗi"+ex.getMessage());
        }
    }

    private void loadRoom() {
        try {
            RoomComboBox.removeAllItems();
            List<Room> rooms = roomDAO.findAll();
            for (Room room : rooms) {
                RoomComboBox.addItem(room.getId() + " - " + room.getName());
            }
        } catch(SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(this,"Lỗi"+ex.getMessage());
        }
    }
    private void getTable() {
        try {         
            model = (DefaultTableModel) tableShowtime.getModel();
            model.setRowCount(0);
            List<Showtime> showTimes = showtimeDAO.getAllShowtimes();
            DateTimeFormatter formatDate = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            DateTimeFormatter formatTime = DateTimeFormatter.ofPattern("HH:mm");
            for (Showtime st : showTimes) {
                model.addRow(new Object[]{
                    st.getId(),
                    st.getMovie().getTitle(),
                    st.getRoom().getName(),
                    st.getDate().format(formatDate),
                    st.getStart_time().format(formatTime)+"-"+st.getEnd_time().format(formatTime),
                    st.getShow_price()                   
                });
            }                       
        } catch(SQLException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(this,"Lỗi"+ex.getMessage());
        }
    }
    private void loadSelected(){
        int selectedRow = tableShowtime.getSelectedRow();
        if (selectedRow != -1) {
            idField.setText((String) model.getValueAt(selectedRow, 0));         
            dateField.setText((String) model.getValueAt(selectedRow, 3));
            String time=((String) model.getValueAt(selectedRow, 4)).split("-")[0];
            timeField.setText(time);
            priceField.setText(String.valueOf(model.getValueAt(selectedRow,5)));
            try {
                Showtime show = showtimeDAO.getShowtimeById(idField.getText());
                if (show != null) {         
                    MovieComboBox.setSelectedItem(show.getMovie().getId() + " - " + show.getMovie().getTitle());
                    RoomComboBox.setSelectedItem(show.getRoom().getId() + " - " + show.getRoom().getName());
                }
            } catch (SQLException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }      
    }    
    private void ClearForm(){
           idField.setText("");
           priceField.setText("");
           dateField.setText("");
           timeField.setText("");
           MovieComboBox.setSelectedIndex(0);
           RoomComboBox.setSelectedIndex(0);
           tableShowtime.clearSelection();
    }
