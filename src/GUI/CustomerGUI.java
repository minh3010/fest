    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        if (CusNameField.getText().isEmpty() || CusNumField.getText().isEmpty() ||
            CusPhoneField.getText().isEmpty() || CusEmailField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }
        try {
            Customer cus=new Customer(
                        CusNumField.getText().trim(),  // Mã khách
                        CusNameField.getText().trim(),  // Tên khách
                        CusPhoneField.getText().trim(),  // SĐT
                        CusEmailField.getText().trim()  // Email
            );
            cusDAO.insertCustomer(cus);
            JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!");
            loadData();
            clearForm();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm khách hàng:\n" + ex.getMessage());
        }
    }//GEN-LAST:event_AddBtnActionPerformed

    private void EditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBtnActionPerformed
        if (CusNumField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã khách hàng cần sửa!");
            return;
        }
        try {
            Customer cus=new Customer(
                        CusNumField.getText().trim(),  // Mã khách
                        CusNameField.getText().trim(),  // Tên khách
                        CusPhoneField.getText().trim(),  // SĐT
                        CusEmailField.getText().trim(),  // Email
                        Integer.parseInt(CusPointField.getText().trim())
            );
            cusDAO.updateCustomer(cus);
            cusDAO.updatePoint(cus);
            JOptionPane.showMessageDialog(this, "Cập nhật khách hàng thành công!");
            loadData();
            clearForm();
            
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật khách hàng:\n" + ex.getMessage());
        }
    }//GEN-LAST:event_EditBtnActionPerformed

    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        model = (DefaultTableModel) custable.getModel();
    int selectedRow = custable.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng cần xóa!");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa khách hàng này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        String id = model.getValueAt(selectedRow, 0).toString(); // Lấy mã KH từ cột đầu tiên
        try {
            cusDAO.deleteCustomer(id);
            JOptionPane.showMessageDialog(this, "Xóa khách hàng thành công!");
            loadData(); // tải lại bảng
            clearForm(); // xóa textfield

        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi khi xóa khách hàng:\n" + ex.getMessage());
        }
    }
    }//GEN-LAST:event_DeleteBtnActionPerformed

    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        String keyword = SearchField.getText().trim();
        if (keyword.isEmpty()) {
            loadData();
            return;
        }
        model = (DefaultTableModel) custable.getModel();
        model.setRowCount(0);
        try {
            List<Customer> customers=cusDAO.searchByPhone(keyword);
            for(Customer cus:customers) {
                model.addRow(new Object[]{
                        cus.getCusID(),
                        cus.getCusName(),
                        cus.getCusPhone(),
                        cus.getCusEmail(),
                        cus.getCusPoint()
                });
            }
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tìm kiếm khách hàng:\n" + ex.getMessage());
        }
    }//GEN-LAST:event_SearchBtnActionPerformed

