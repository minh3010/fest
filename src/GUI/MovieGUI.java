    private void getTable(){
       String sql="select * from movie";
       
       try{
          Connection con=Database.getDB().connect();
          PreparedStatement pst=con.prepareStatement(sql);
          ResultSet rs=pst.executeQuery();
          model=(DefaultTableModel)tableMovie.getModel();
          model.setRowCount(0);
          while(rs.next()){
             model.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getInt(5),rs.getBoolean(6)});
          }
       }catch(SQLException | ClassNotFoundException ex){
          JOptionPane.showMessageDialog(this,"Lỗi");
       }
    } 
    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            Movie movie= new Movie(
                    IdField.getText().trim(),
                    titleField.getText().trim(),
                    genreField.getText().trim(),
                    directorField.getText().trim(),
                    Integer.parseInt(durationField.getText().trim()),
                    ShowingCheckBox.isSelected()
            );
            movieDAO.addMovie(movie);
            JOptionPane.showMessageDialog(this,"Thêm thành công");
            getTable();
            ClearForm();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this,"Lỗi thêm phim");
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Thời lượng phải định dạng số");
        }
           
    }//GEN-LAST:event_addBtnActionPerformed

